// Helper to find location by slug
export const getLocationBySlug = (slug) => {
  return LOCATIONS.find(loc => loc.slug === slug) || null;
};

// Helper to find location by name (partial match)
export const getLocationByName = (name) => {
  const lower = name.toLowerCase();
  return LOCATIONS.find(loc =>
    loc.name.toLowerCase().includes(lower) ||
    loc.slug.includes(lower.replace(/\s+/g, '-'))
  ) || null;
};
